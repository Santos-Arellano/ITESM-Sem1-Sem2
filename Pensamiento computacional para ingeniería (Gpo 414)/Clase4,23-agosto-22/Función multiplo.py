"""Función multiplo de que recibe dos datos: el primero es el número a
comprobar, el segundo es el divisor. La función debe devolver True, si el
primer número es múltiplo del segundo y False en caso contrario."""

def funcion_multiplo_de(num1,num2):
    if num1%num2==0:
        print ("True")
    else:
        print ("False")